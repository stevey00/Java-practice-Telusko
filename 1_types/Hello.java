/**
 * In Java programming, there are two categories of data types: primitive data types and reference data types.

    Primitive Data Types:
     
    Integer Types:
 * byte: 8-bit integer
 * short: 16-bit integer
 * int: 32-bit integer
 * long: 64-bit integer
 * Floating-Point Types:
 * float: 32-bit floating-point
 * double: 64-bit floating-point
 
    Character Type:
 * char: 16-bit Unicode character

    Boolean Type:
 * boolean: represents true or false

    Reference Data Types:

 * Class Types: Instances of user-defined classes
 * Interface Types: Instances of interfaces
 * Array Types: Arrays of any data type, including primitives or objects
 * Enumeration Types: Instances of enumerated types
 * Annotated Types: Instances of annotated elements in the Java programming language

Primitive data types are basic data types built into the Java language, whereas reference data types 
refer to objects and are more complex. Each data type has its own size and range of values.

 */


 
